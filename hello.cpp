// C++ program to illustrate for loop to print numbers from 
// 1 to n 

#include <iostream> 
using namespace std; 

int main() 
{ 
	// initializing n (value upto which you want to print 
	// numbers 
	int n = 5; 
	int i; // initialization of loop variable 
	for (i = 1; i <= n; i++) { 
		cout << i << " "; 
	} 

	return 0; 
}
